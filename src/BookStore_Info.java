    private void jMenuItemSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemSaveActionPerformed
        //Saving the list of books 
        JFileChooser fileChooser = new JFileChooser();//creating new instance of file chooser class
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));//setting current directory of user to
        int userSelection = fileChooser.showSaveDialog(this);
        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            String filePath = fileToSave.getAbsolutePath();
            File file = new File(filePath);
            try {
                FileWriter fw = new FileWriter(file);
                BufferedWriter bw = new BufferedWriter(fw);

                for(int i = 0; i < jTable1.getRowCount(); i++){//rows
                    for(int j = 0; j < jTable1.getColumnCount(); j++){//columns
                        bw.write(jTable1.getValueAt(i, j).toString()+"/");// Seperating columns through "/"
                    }
                    bw.newLine();
                }

                bw.close();
                fw.close();
                JOptionPane.showMessageDialog(this, "Data Saved!","Successful",JOptionPane.INFORMATION_MESSAGE);
            } catch (IOException ex) {
                Logger.getLogger(BookStore_Info.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jMenuItemSaveActionPerformed

