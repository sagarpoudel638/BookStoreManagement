    private void jMenuItemOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemOpenActionPerformed
        //Opening the list of books 
        JFileChooser fileChooser = new JFileChooser();//creating new instance of file chooser class
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));//setting current directory of user to
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            String filePath = selectedFile.getAbsolutePath();
            File file = new File(filePath);

            try {
                FileReader fr = new FileReader(file);
                BufferedReader br = new BufferedReader(fr);

                DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                Object[] lines = br.lines().toArray();
                //Clear all the data from table
                while(model.getRowCount()>0){
                    for(int i=0; i< model.getRowCount(); i++){
                        model.removeRow(i);
                    }
                }

                for(int i = 0; i < lines.length; i++){
                    String[] row = lines[i].toString().split("/");// Staring in new column where seperated by "/"
                    model.addRow(row);
                }
                //Converting Price Column to integer
                for(int i = 0; i < jTable1.getRowCount(); i++){
                    int pricecon= Integer.parseInt(jTable1.getValueAt(i, 5).toString());//Convertiing the Price column to integer
                   jTable1.setValueAt(pricecon, i, 5);
                }

            } catch (FileNotFoundException ex) {
                Logger.getLogger(BookStore_Info.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jMenuItemOpenActionPerformed

    private void AvailableBooksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AvailableBooksActionPerformed
        // Displaying books available in given genre
        String SearchGenre= GenreCombo.getSelectedItem().toString();
        if(SearchGenre.equals("Select Genre")){
            JOptionPane.showMessageDialog(this, "Please Select Genre","Alert",JOptionPane.WARNING_MESSAGE);
        }
        else{
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();//Getting details about the table
            String Available="";
            int count=0;
            for(int i=0; i< model.getRowCount(); i++){
                String AvailableBooks= jTable1.getValueAt(i, 2).toString();//Storing books genre name
                if(SearchGenre.equals(AvailableBooks)){//checking whether the user specified genre matches the AvailableBooks variable or not
                    Available= Available + jTable1.getValueAt(i, 0) + " :- " + jTable1.getValueAt(i, 1)+"\n";//Storing the matched books in string
                    count=count+1;//counting number of books available in specified genre
                }    
            }
            JOptionPane.showMessageDialog(this,"There are "+count+" books available:"+"\n" +Available,"Available Books for" + " " + SearchGenre,JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_AvailableBooksActionPerformed

