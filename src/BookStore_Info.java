    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        //Sorting all the data in table based on price
        TableRowSorter<TableModel> sorter = new TableRowSorter<>(jTable1.getModel());
        jTable1.setRowSorter(sorter);
        ArrayList<RowSorter.SortKey> sortKeys = new ArrayList<>();
        int columnIndexToSort = 5;// Specifying the column index of Price
        sortKeys.add(new RowSorter.SortKey(columnIndexToSort, SortOrder.ASCENDING));
        sorter.setSortKeys(sortKeys);
        sorter.sort();
        price.removeAll(price);
        // validation for search pannel
        String SearchPrice=jTextFieldSearchPrice.getText();
        //checking whether the fields are empty or not
        if(SearchPrice.equals("")){
            JOptionPane.showMessageDialog(this, "Please Enter Price.","Alert",JOptionPane.WARNING_MESSAGE);
        }
        else if(!SearchPrice.equals("")){// Checking price validation
            try{
                   int SearchPriceValid=Integer.parseInt(SearchPrice);
                   DefaultTableModel model = (DefaultTableModel)jTable1.getModel();//Getting details about the table
                    for(int i=0; i< model.getRowCount(); i++){
                        String pricestr= jTable1.getValueAt(i, 5).toString();//Converting object to string
                        price.add(Integer.parseInt(pricestr));//Adding the sorted price to array after converting string to integer
                    }
                    //Binary Searching starts
                    int last = price.size()-1;
                    int first=0;
                    int mid = (first+last)/2;
                    while(first<=last){
                        if(price.get(mid)< SearchPriceValid){
                            first= mid + 1;
                        }else if(price.get(mid)== SearchPriceValid){
                            JOptionPane.showMessageDialog(this, "The book according to your price is:" + "\n" + "Book Code: " + jTable1.getValueAt(mid,0)+ "\n" +"Book Name: "+ jTable1.getValueAt(mid,1) + "\n"+ "Language: "+  jTable1.getValueAt(mid,3)+"\n"+ "Genre: " + jTable1.getValueAt(mid,2) + "\n" + "Author: " + jTable1.getValueAt(mid,4) ,"Data Found",JOptionPane.INFORMATION_MESSAGE);
                            break;
                        }else{
                            last=mid-1;
                        }
                        mid=(first+last)/2;
                    }
                    if(first > last){
                        JOptionPane.showMessageDialog(this, "No data found.","Price not matched",JOptionPane.WARNING_MESSAGE);
                    }
                    //Binary search ends
                }catch(NumberFormatException e){
                    JOptionPane.showMessageDialog(this, "Please Enter Valid Price.","Alert",JOptionPane.WARNING_MESSAGE); // Price validation (Checks integer or not)
                }
        }       
    }//GEN-LAST:event_jButtonSearchActionPerformed

